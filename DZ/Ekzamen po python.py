'''Задание 4.
Дается две строки:
num = "0123456789" и bkv = "цБиУфКрВыЫ"
На четных позициях должны находится четные цифры, 
на нечетных позициях - буквы.'''

num = "0123456789"  # Все цифры
bkv = "цБиУфКрВыЫ"  # Буквы

result = ""
numx = 0
bkvx = 0

# Определяем количество четных цифр и букв
num_length = len(num) // 2  # Количество четных цифр
bkv_length = len(bkv)       # Количество букв

# Максимальное количество итераций, ограниченное числом четных цифр и букв
max_length = max(num_length, bkv_length)

for i in range(max_length):
    # Добавляем четную цифру, если она доступна
    if numx < num_length:
        result += num[numx * 2]  # Четные цифры: 0, 2, 4, 6, 8
        numx += 1

    # Добавляем букву, если она доступна
    if bkvx < bkv_length:
        result += bkv[bkvx]  # Буквы идут по порядку
        bkvx += 1

print(result)

